int add(int a, int b)
{
	int sum = a + b;
	return (sum);
}

int sub(int a, int b)
{
	int min = a - b;
	return (min);
}

int mul(int a, int b)
{
	int pro = a * b;
	return (pro);
}

int div(int a, int b)
{
	int quo = a / b;
	return (quo);
}

int mod(int a, int b)
{
	int mo = a % b;
	return (mo);
}
